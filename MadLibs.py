# Pedir al usuario que ingrese palabras
nombre = input("Ingresa un nombre: ")
verbo = input("Ingresa un verbo: ")
sustantivo = input("Ingresa un sustantivo: ")
adjetivo = input("Ingresa un adjetivo: ")

# Crear una historia utilizando las palabras proporcionadas por el usuario
historia = f"{nombre} {verbo} {sustantivo} {adjetivo}."

print("\nTu historia ha sido generada espero que sea de tu agrado:):")
print(historia)

